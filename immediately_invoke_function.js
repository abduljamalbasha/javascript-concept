/**
 * immediately invoke function:
 * 
 * anonymous functions is to invoke the function immediately after initialization, this is also known as Self Executing Function. 
 * This can be done by adding parenthesis we can immediately execute the anonymous function.
 */

(function (name){
    console.log('Hello am immediately invoke function/ Self Executing Funtion')
    console.log("Hello", name)
})("Ayana")